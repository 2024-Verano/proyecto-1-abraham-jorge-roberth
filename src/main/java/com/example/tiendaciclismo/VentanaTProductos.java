    private void elimarNombreBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_elimarNombreBtnActionPerformed
        // TODO add your handling code here:
        String nombre = elimNameTf.getText().trim();
        if (!nombre.isEmpty()) {
            int respuesta = JOptionPane.showConfirmDialog(this,
                    "¿Está seguro de que desea eliminar el TipoProducto con el nombre \"" + nombre + "\"?",
                    "Confirmación de eliminación",
                    JOptionPane.YES_NO_OPTION);

            if (respuesta == JOptionPane.YES_OPTION) {
                try {
                    boolean eliminado = registro.eliminarTipoProducto(nombre);
                    if (eliminado) {
                        JOptionPane.showMessageDialog(this, "TipoProducto eliminado correctamente.");
                        elimNameTf.setText("");
                    } else {
                        JOptionPane.showMessageDialog(this, "No se encontró un TipoProducto con ese nombre.");
                    }
                } catch (Exception ex) {
                    ex.printStackTrace();
                    JOptionPane.showMessageDialog(this, "Ocurrió un error al eliminar.");
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "Ingrese un nombre válido.");
        }

    }//GEN-LAST:event_elimarNombreBtnActionPerformed

