    private void saveModBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveModBtnActionPerformed
        // Get input values
        String nuevoNombre = modNombreTf.getText().trim();
        String nombre = busquedaNombreTf1.getText().trim();
        String codigoTexto = busquedaNombreTf2.getText().trim();

        try {
            //valida si no esta vacio
            if (nuevoNombre.isEmpty()) {
                JOptionPane.showMessageDialog(this, "El nuevo nombre no puede estar vacio.", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            if (!nombre.isEmpty()) {
                //modifca por nombre
                registro.modificarTipoProductoN(nombre, nuevoNombre);
                JOptionPane.showMessageDialog(this, "Se modifico correctamente por nombre.", "Exito", JOptionPane.INFORMATION_MESSAGE);
                busquedaNombreTf1.setText(""); // Clear fields
            } else if (!codigoTexto.isEmpty()) {
                // valida y modififica por codigo
                long codigo = Long.parseLong(codigoTexto);
                registro.modificarTipoProductoC(codigo, nuevoNombre);
                JOptionPane.showMessageDialog(this, "Se modificó correctamente por código.", "Éxito", JOptionPane.INFORMATION_MESSAGE);
                busquedaNombreTf2.setText(""); // Clear fields
            } else {
                //ambas vacias
                JOptionPane.showMessageDialog(this, "Debe ingresar un nombre o código para buscar y modificar.", "Error", JOptionPane.ERROR_MESSAGE);
            }

            modNombreTf.setText(""); // limpiar espacios
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "El código ingresado debe ser un número válido.", "Error", JOptionPane.ERROR_MESSAGE);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Ocurrió un error al modificar: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            Logger.getLogger(VentanaTProductos.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_saveModBtnActionPerformed

