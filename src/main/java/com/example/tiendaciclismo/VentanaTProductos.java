    private void acceptAddBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_acceptAddBtnActionPerformed
        // TODO add your handling code here:
        String nombre = addTf.getText();
        String trimmedNombre = nombre.trim();
        String regex = "^[a-zA-Z]+$"; //expression de letras

        if (!nombre.matches(trimmedNombre) || !nombre.matches(regex)) {

            JOptionPane.showMessageDialog(this, "No es un nombre valido: Intentalo de nuevo");

            addTf.setText("");
        } else {
            registro.agregarTipoProducto(nombre);
            registro.guardarTipos();

            JOptionPane.showMessageDialog(this, "Se ha cargado exitosamente.");

            addTf.setText("");
        }
    }//GEN-LAST:event_acceptAddBtnActionPerformed

